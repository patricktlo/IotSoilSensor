/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_COMP1_Init();
  MX_ADC_Init();
  MX_TIM21_Init();
  MX_TIM2_Init();
  MX_TIM22_Init();
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */

  // Init All
  ADC_calibrate();
  MS_init();
  NTC_init();
  HR202_init();

  SX1278_t LoRa;
  SX1278_hw_t LoRaHW;
  LoRa_init(&LoRa, &LoRaHW);

  uint16_t message_nb = 0;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	    GPIO_userLed_on();

	    // variable containing the payload
		uint8_t txsend[PAYLOAD_SIZE];
		uint32_t rh_value, temperature_value, MS_value;

		// Get values
		temperature_value = NTC_getTemp();
		MS_value = MS_getMS();
		rh_value = HR202_getRH();

		// Sort values into bytes in order to send them
		LoRa_parsePayload(txsend, temperature_value, MS_value, rh_value, message_nb);

		// Send data via Lora
		SX1278_LoRaEntryTx(&LoRa, PAYLOAD_SIZE, 2000);
		SX1278_LoRaTxPacket(&LoRa, (uint8_t *) txsend, PAYLOAD_SIZE, 2000);

		// Put LoRa module to sleep
		SX1278_sleep(&LoRa);

		// Increment message nb
		message_nb++;

	    GPIO_userLed_off();

		// Stop
	    SCB->SCR |= SCB_SCR_SLEEPDEEP_Msk; // low-power mode = stop mode
		PWR->CR &= ~PWR_CR_PDDS;
		PWR->CSR &= ~(PWR_CSR_WUF);

		// Select MSI as stop clock
		RCC->CFGR &= ~(RCC_CFGR_STOPWUCK);

		__WFI();


//		HAL_Delay(1000);


  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

